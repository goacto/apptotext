/**
 * Escapes HTML special characters to prevent XSS.
 */
function escapeHtml(text: string): string {
  const map: Record<string, string> = {
    "&": "&amp;",
    "<": "&lt;",
    ">": "&gt;",
    '"': "&quot;",
    "'": "&#039;",
  };
  return text.replace(/[&<>"']/g, (char) => map[char]);
}

/**
 * Simple markdown-to-HTML converter for PDF export.
 * Handles common markdown patterns without external dependencies.
 */
function markdownToHtml(markdown: string): string {
  let html = markdown;

  // Escape HTML in the raw text first, but preserve markdown syntax
  // We process block-level elements first, then inline

  // Code blocks (fenced) — must be done before escaping
  const codeBlocks: string[] = [];
  html = html.replace(/```(\w*)\n([\s\S]*?)```/g, (_match, _lang, code) => {
    const index = codeBlocks.length;
    codeBlocks.push(`<pre><code>${escapeHtml(code.trimEnd())}</code></pre>`);
    return `%%CODEBLOCK_${index}%%`;
  });

  // Inline code — must be done before escaping
  const inlineCodes: string[] = [];
  html = html.replace(/`([^`]+)`/g, (_match, code) => {
    const index = inlineCodes.length;
    inlineCodes.push(`<code>${escapeHtml(code)}</code>`);
    return `%%INLINECODE_${index}%%`;
  });

  // Now escape remaining HTML
  html = escapeHtml(html);

  // Restore code blocks and inline codes
  codeBlocks.forEach((block, i) => {
    html = html.replace(`%%CODEBLOCK_${i}%%`, block);
  });
  inlineCodes.forEach((code, i) => {
    html = html.replace(`%%INLINECODE_${i}%%`, code);
  });

  // Headings
  html = html.replace(/^#### (.+)$/gm, "<h4>$1</h4>");
  html = html.replace(/^### (.+)$/gm, "<h3>$1</h3>");
  html = html.replace(/^## (.+)$/gm, "<h2>$1</h2>");
  html = html.replace(/^# (.+)$/gm, "<h1>$1</h1>");

  // Blockquotes
  html = html.replace(/^&gt; (.+)$/gm, "<blockquote>$1</blockquote>");

  // Bold and italic
  html = html.replace(/\*\*\*(.+?)\*\*\*/g, "<strong><em>$1</em></strong>");
  html = html.replace(/\*\*(.+?)\*\*/g, "<strong>$1</strong>");
  html = html.replace(/\*(.+?)\*/g, "<em>$1</em>");

  // Links
  html = html.replace(
    /\[([^\]]+)\]\(([^)]+)\)/g,
    '<a href="$2">$1</a>'
  );

  // Horizontal rules
  html = html.replace(/^---$/gm, "<hr>");

  // Unordered lists
  html = html.replace(/^(\s*)[-*+] (.+)$/gm, "$1<li>$2</li>");

  // Ordered lists
  html = html.replace(/^(\s*)\d+\. (.+)$/gm, "$1<li>$2</li>");

  // Wrap consecutive <li> elements in <ul>
  html = html.replace(/((?:<li>.*<\/li>\n?)+)/g, "<ul>$1</ul>");

  // Paragraphs — wrap lines not already in block elements
  const blockTags = ["<h", "<ul", "<ol", "<pre", "<blockquote", "<hr", "<li", "<div", "<table"];
  const processedLines = html.split("\n");
  const outputLines: string[] = [];
  let inParagraph = false;

  for (const line of processedLines) {
    const trimmed = line.trim();

    if (trimmed === "") {
      if (inParagraph) {
        outputLines.push("</p>");
        inParagraph = false;
      }
      continue;
    }

    const isBlockElement = blockTags.some((tag) => trimmed.startsWith(tag));

    if (isBlockElement) {
      if (inParagraph) {
        outputLines.push("</p>");
        inParagraph = false;
      }
      outputLines.push(trimmed);
    } else {
      if (!inParagraph) {
        outputLines.push("<p>");
        inParagraph = true;
      }
      outputLines.push(trimmed);
    }
  }

  if (inParagraph) {
    outputLines.push("</p>");
  }

  return outputLines.join("\n");
}
