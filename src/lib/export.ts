import { APP_NAME, GOACTO_FULL, GOACTO_SHORT, TEXTBOOK_LEVELS } from "@/lib/constants";
import type { Conversion, TextbookChapter } from "@/lib/types";

/**
 * Generates a formatted Markdown string from a conversion and its chapters.
 */
export function exportToMarkdown(
  conversion: Conversion,
  chapters: TextbookChapter[]
): string {
  const date = new Date(conversion.created_at).toLocaleDateString("en-US", {
    year: "numeric",
    month: "long",
    day: "numeric",
  });

  const lines: string[] = [];

  // Title page
  lines.push(`# ${conversion.title}`);
  lines.push("");
  lines.push(`**Source:** ${conversion.source_url}`);
  lines.push(`**Date:** ${date}`);
  lines.push(`**AI Provider:** ${conversion.ai_provider}`);
  lines.push(`**Generated by:** ${APP_NAME}`);
  lines.push("");

  // Table of contents
  lines.push("## Table of Contents");
  lines.push("");

  for (const levelMeta of TEXTBOOK_LEVELS) {
    const levelChapters = chapters.filter((ch) => ch.level === levelMeta.level);
    if (levelChapters.length === 0) continue;

    lines.push(`### Level ${levelMeta.level}: ${levelMeta.name}`);
    lines.push("");
    for (const chapter of levelChapters) {
      lines.push(`- Chapter ${chapter.chapter_number}: ${chapter.title}`);
    }
    lines.push("");
  }

  lines.push("---");
  lines.push("");

  // Each level and its chapters
  for (const levelMeta of TEXTBOOK_LEVELS) {
    const levelChapters = chapters.filter((ch) => ch.level === levelMeta.level);
    if (levelChapters.length === 0) continue;

    lines.push(`## Level ${levelMeta.level}: ${levelMeta.name}`);
    lines.push("");
    lines.push(`> ${levelMeta.description}`);
    lines.push("");

    for (const chapter of levelChapters) {
      lines.push(`### Chapter ${chapter.chapter_number}: ${chapter.title}`);
      lines.push("");
      lines.push(chapter.content);
      lines.push("");

      if (chapter.key_concepts && chapter.key_concepts.length > 0) {
        lines.push("#### Key Concepts");
        lines.push("");
        for (const concept of chapter.key_concepts) {
          lines.push(`- ${concept}`);
        }
        lines.push("");
      }
    }

    lines.push("---");
    lines.push("");
  }

  // Footer
  lines.push(`*Generated by ${APP_NAME} | ${GOACTO_SHORT} — ${GOACTO_FULL}*`);
  lines.push("");

  return lines.join("\n");
}

/**
 * Creates a Blob from the markdown content and triggers a file download.
 */
export function downloadMarkdown(filename: string, content: string): void {
  const blob = new Blob([content], { type: "text/markdown;charset=utf-8" });
  const url = URL.createObjectURL(blob);

  const anchor = document.createElement("a");
  anchor.href = url;
  anchor.download = filename;
  anchor.style.display = "none";

  document.body.appendChild(anchor);
  anchor.click();

  // Cleanup
  document.body.removeChild(anchor);
  URL.revokeObjectURL(url);
}

